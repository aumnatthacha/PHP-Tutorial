echo "<font color='#993300'><p>- $x = 1; - เริ่มต้นตัวนับลูป ($x) และตั้งค่าเริ่มต้นเป็น 1</p>";
echo "<font color='#993300'><p>- $x <= 5 - วนลูปต่อไปตราบเท่าที่ $x น้อยกว่าหรือเท่ากับ 5</p>";
echo "<font color='#993300'><p>- $x++; - เพิ่มค่าตัวนับลูปทีละ 1 สำหรับการวนซ้ำแต่ละครั้ง</p>";
echo "<font color='#993300'><p>ตัวอย่างนี้นับเป็น 100 คูณสิบ</p>";
$x = 0;
