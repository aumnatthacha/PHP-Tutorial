<?php
echo "<font color='#993300'><h1>PHP ในขณะที่วนซ้ำ</h1>";
echo "<font color='#993300'><p>การwhileวนซ้ำ - วนซ้ำบล็อกของโค้ดตราบเท่าที่เงื่อนไขที่ระบุเป็นจริง</p>";
echo "<font color='#993300'><p>ลูป while ดำเนินการบล็อกของรหัสตราบเท่าที่เงื่อนไขที่ระบุเป็นจริง</p>";
echo "<font color='#993300'><p>ตัวอย่างด้านล่างแสดงตัวเลขตั้งแต่ 1 ถึง 5</p>";
$x = 1;
while($x <= 5) {
  echo "The number is: $x <br>";
  $x++;
}

